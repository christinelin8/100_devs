document.getElementById('pink').onclick = blossomPink
document.getElementById('green').onclick = leafGreen
document.getElementById('yellow').onclick = pokemonYellow
document.getElementById('purple').onclick = justPurple

function blossomPink() {
  document.querySelector('body').style.backgroundColor = 'rgb(253, 202, 211)'
  document.querySelector('body').style.color = 'white'
}

function leafGreen() {
  document.querySelector('body').style.backgroundColor = 'rgb(131, 171, 131)'
  document.querySelector('body').style.color = 'white'
}

function pokemonYellow() {
  document.querySelector('body').style.backgroundColor = 'rgb(237, 237, 162)'
  document.querySelector('body').style.color = 'white'
}

function justPurple() {
  document.querySelector('body').style.backgroundColor = 'rgb(182, 121, 182)'
  document.querySelector('body').style.color = 'white'
}




